public class FloatDouble {
    public static void main(String[] args) {

        double radius = 10.8f;
        double pi = 3.14f;
        double area = pi * radius * radius;
        System.out.println(area);

        float radius_f = 10.8f;
        float pi_f = 3.14f;
        float area_f = pi_f * radius_f * radius_f;

        System.out.println(area_f);
    }
}
